package Operadores;

public class OperadoresRelacionais {
	public static void main(String[] args) {
	int valor1 = 1;
	int valor2 = 2;
	
	// verificar se dois valores sao IGUAIS
	System.out.println("valor1 == valor2: " + (valor1 == valor2));
	
	// verificar se dois valores sao DIFERENTES
	System.out.println("valor1 != valor2: " + (valor1 != valor2));
	
	// verificar se o valor1 é MAIOR que o valor2
	System.out.println("valor1 > valor2: " + (valor1 > valor2));
	
	// verificar se o valor1 é MAIOR OU IGUAL que o valor2
	System.out.println("valor1 >= valor2: " + (valor1 >= valor2));
	
	// verificar se o valor1 é MENOR que o valor2
	System.out.println("valor1 < valor2: " + (valor1 < valor2));
	
	// verificar se o valor1 é MENOR OU IGUAL que o valor2
	System.out.println("valor1 <= valor2: " + (valor1 <= valor2));
}
}
